//случайные индексы из массива
function getRandomValuesFromArray(array, count) {
  let randomValues = [];

  for (let i = 0; i < count; i++) {
    let randomIndex = Math.floor(Math.random() * array.length); // генерируем случайный индекс
    randomValues.push(array[randomIndex]); // добавляем значение из массива в новый массив
  }

  return randomValues;
}
